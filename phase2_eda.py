def eda_combined(conn):
    print("\n" + "=" * 60)
    print("  COMBINED INSIGHTS — Bus vs Subway")
    print("=" * 60)

    run_query(conn, """
        SELECT 'Bus'    AS mode, ROUND(AVG(delay_minutes), 2) AS avg_delay_min,
               COUNT(*) AS total_incidents,
               ROUND(SUM(delay_minutes)/60.0, 1) AS total_delay_hours
        FROM bus_delays WHERE delay_minutes > 0
        UNION ALL
        SELECT 'Subway', ROUND(AVG(delay_minutes), 2),
               COUNT(*),
               ROUND(SUM(delay_minutes)/60.0, 1)
        FROM subway_delays WHERE delay_minutes > 0
    """, "9. Bus vs Subway — Head to Head Summary")

    run_query(conn, """
        SELECT 'Bus' AS mode, hour, COUNT(*) AS incidents
        FROM bus_delays
        WHERE delay_minutes > 0 AND hour IS NOT NULL
        GROUP BY hour
        ORDER BY incidents DESC
        LIMIT 1
    """, "10a. Bus — Single Peak Delay Hour")

    run_query(conn, """
        SELECT 'Subway' AS mode, hour, COUNT(*) AS incidents
        FROM subway_delays
        WHERE delay_minutes > 0 AND hour IS NOT NULL
        GROUP BY hour
        ORDER BY incidents DESC
        LIMIT 1
    """, "10b. Subway — Single Peak Delay Hour")


