import sqlite3
import pandas as pd

DB_PATH = "ttc_delays.db"


def run_query(conn, sql, title):
    print(f"\n{'─' * 60}")
    print(f"  {title}")
    print('─' * 60)
    df = pd.read_sql(sql, conn)
    print(df.to_string(index=False))
    return df


def eda_bus(conn):
    print("\n" + "=" * 60)
    print("  BUS DELAYS — EDA")
    print("=" * 60)

    run_query(conn, """
        SELECT
            COUNT(*)                              AS total_incidents,
            ROUND(AVG(delay_minutes), 2)          AS avg_delay_min,
            MAX(delay_minutes)                    AS max_delay_min,
            ROUND(SUM(delay_minutes) / 60.0, 1)  AS total_delay_hours
        FROM bus_delays
        WHERE delay_minutes > 0
    """, "1. Overall Bus Delay Summary")

    run_query(conn, """
        SELECT
            Code                            AS delay_code,
            COUNT(*)                        AS incidents,
            ROUND(AVG(delay_minutes), 1)    AS avg_delay_min,
            MAX(delay_minutes)              AS max_delay_min
        FROM bus_delays
        WHERE delay_minutes > 0
          AND Code IS NOT NULL
        GROUP BY Code
        ORDER BY incidents DESC
        LIMIT 10
    """, "2. Top 10 Bus Delay Codes (by frequency)")

    run_query(conn, """
        SELECT
            Line                            AS route,
            COUNT(*)                        AS incidents,
            ROUND(AVG(delay_minutes), 1)    AS avg_delay_min,
            ROUND(SUM(delay_minutes), 0)    AS total_delay_min
        FROM bus_delays
        WHERE delay_minutes > 0
          AND Line IS NOT NULL
        GROUP BY Line
        ORDER BY incidents DESC
        LIMIT 10
    """, "3. Top 10 Worst Bus Routes by Incident Count")

    run_query(conn, """
        SELECT
            day_of_week,
            COUNT(*)                        AS incidents,
            ROUND(AVG(delay_minutes), 1)    AS avg_delay_min
        FROM bus_delays
        WHERE delay_minutes > 0
        GROUP BY day_of_week
        ORDER BY incidents DESC
    """, "4. Bus Delays by Day of Week")

    run_query(conn, """
        SELECT
            hour,
            COUNT(*)                        AS incidents,
            ROUND(AVG(delay_minutes), 1)    AS avg_delay_min
        FROM bus_delays
        WHERE delay_minutes > 0
          AND hour IS NOT NULL
        GROUP BY hour
        ORDER BY hour
    """, "5. Bus Delays by Hour of Day")

    run_query(conn, """
        SELECT
            year,
            month,
            COUNT(*)                        AS incidents,
            ROUND(AVG(delay_minutes), 1)    AS avg_delay_min
        FROM bus_delays
        WHERE delay_minutes > 0
        GROUP BY year, month
        ORDER BY year, month
    """, "6. Bus Delay Monthly Trend")

    run_query(conn, """
        SELECT
            date,
            time,
            Line        AS route,
            Station     AS location,
            Code        AS delay_code,
            delay_minutes
        FROM bus_delays
        ORDER BY delay_minutes DESC
        LIMIT 10
    """, "7. Top 10 Worst Individual Bus Delays")


